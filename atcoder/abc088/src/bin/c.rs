use proconio::input;

fn f(c: &[Vec<i64>]) -> bool {
    c[0][1] - c[0][0] == c[1][1] - c[1][0]
        && c[0][1] - c[0][0] == c[2][1] - c[2][0]
        && c[0][2] - c[0][0] == c[1][2] - c[1][0]
        && c[0][2] - c[0][0] == c[2][2] - c[2][0]
        && c[1][0] - c[0][0] == c[1][1] - c[0][1]
        && c[1][0] - c[0][0] == c[1][2] - c[0][2]
        && c[2][0] - c[0][0] == c[2][1] - c[0][1]
        && c[2][0] - c[0][0] == c[2][2] - c[0][2]
}

fn main() {
    input! {
        c: [[i64; 3]; 3],
    }
    let yes = f(&c);
    println!("{}", if yes { "Yes" } else { "No" });
}
